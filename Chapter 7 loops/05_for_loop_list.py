# # for loop with lists
l = [1, 4, 5, 6, 6, 56, 46] # for list
for i in l:
    print(i)


## for loop with tuple
t = (5, 45 ,455, 45) # for tuple
for i in t:
    print(i)


## for loop withs sting
s = ["harry", "atul"] # for sting
for i in s:
    print(i)



## for loops with sets
s1 = {24, 24, 545 } # for set
for i in s1:
    print(s1)